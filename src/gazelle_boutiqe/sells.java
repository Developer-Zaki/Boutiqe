             Class.forName("com.mysql.jdbc.Driver");
            com.mysql.jdbc.Connection conn=(com.mysql.jdbc.Connection)DriverManager.getConnection("jdbc:mysql://localhost/gezele", "root","");
            String sql="select *from customer where id=?";
            PreparedStatement ptst=conn.prepareStatement(sql);

            String id2=code1.getText();
            double id1=Double.parseDouble(id2);

            ptst.setString(1, id2);

            ResultSet rs=ptst.executeQuery();

            DefaultTableModel tm=(DefaultTableModel)sells1.getModel();
