    private void total_priceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_total_priceActionPerformed
        // TODO add your handling code here:
        int sum=0;
        int sum2=0;
        int value2;
        int value;
        int finalSum=0;
        int row=(int) sells1.getRowCount();
        int row2=(int) sells1.getRowCount();
        for(int i=0; i<row; i++){
            value=(int) sells1.getValueAt(i, 3);
            sum +=value;
        }
        for(int j=0; j<row2; j++){
            value2=(int) sells1.getValueAt(j, 4);
            sum2 +=value2;
        }
        finalSum=sum-sum2;
        price.setText("مجموع=  "+ finalSum);
    }//GEN-LAST:event_total_priceActionPerformed

