            while(rs.next()){
                Object o[]={rs.getString("name_Lname"),rs.getInt("id"), rs.getString("pro_name"), rs.getInt("price"),
                    rs.getInt("discount"),rs.getInt("num_item"), rs.getDate("date")};
                tm.addRow(o);
            }

